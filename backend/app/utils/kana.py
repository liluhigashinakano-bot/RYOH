# ひらがな → 全角カタカナのオフセット（U+3041〜U+3096 → U+30A1〜U+30F6）
_HIRAGANA_START = 0x3041
_HIRAGANA_END = 0x3096
_KATA_OFFSET = 0x60


def to_halfwidth_katakana(text: str) -> str:
    """ひらがな・全角カタカナを半角カタカナに変換する。
    それ以外の文字（漢字・英数・記号等）はそのまま残す。"""
    if not text:
        return text
    result = []
    for ch in text:
        cp = ord(ch)
        # ひらがな → まず全角カタカナに変換
        if _HIRAGANA_START <= cp <= _HIRAGANA_END:
            ch = chr(cp + _KATA_OFFSET)
        # 全角カタカナ → 半角カタカナ
        if ch in _FULL_TO_HALF:
            result.append(_FULL_TO_HALF[ch])
        else:
            result.append(ch)
    return ''.join(result)
